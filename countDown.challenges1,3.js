//Challenge 1 (A Basic While Loop)
//countdown.js
var num = 10;

while (num > 0) {
  console.log(num);
  num--;
}


//Challenge 3 (From a While to a For Loop)

//countdown.js
for (var num = 10; num > 0; num--) {
  console.log(num);
}

//Challenge 6 (The Basic Conditional)

//countdown.js
var num = 10;

while (num > 0) {
  if (num % 2 == 0) {
    console.log(num);
  }
  num--;
}
